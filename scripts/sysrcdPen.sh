#!/bin/bash

main () {    
    [ "$1" = "" ] && uso || DEST="$1"
    #read -p "Estás seguro de querer formatear $DEST, todos los datos se perderán?(sS/nN) " RESP
    #[ "$RESP" != "S" ] && [ "$RESP" != "s" ] && uso
    [ "$(sudo fdisk -l | grep $DEST)" = "" ] && echo no encuentro dispositivo $DEST && uso
    [ "$(sudo fdisk -l | grep $DEST | grep -i FAT)" = "" ] && echo Debes formatear el pendrive con FAT && uso
    [ "$2" = "" ] && ISO=$(descargaSysrcd) || ISO="$2"
    [ "$3" = "" ] && LABEL="RESCATE" || LABEL="$3"
    echo "DEST: $DEST ISO: $ISO LABEL: $LABEL"
    PART_DEST="$(sudo fdisk -l | grep $DEST | tail -1 | awk '{print $1;}')"
    MONTAJE_DEST="/tmp/mnt$PART_DEST"
    MONTAJE_ISO="/tmp/sysrcd.iso"
    mkdir -p $MONTAJE_DEST
    mkdir $MONTAJE_ISO
    sudo mount $PART_DEST $MONTAJE_DEST
    sudo mount -o loop $ISO $MONTAJE_ISO
    sudo mkdir -p $MONTAJE_DEST/distros/sysrcd
    for f in isolinux/initram.igz  isolinux/rescue32  isolinux/rescue64  sysrcd.dat  sysrcd.md5; do
        sudo cp -v $MONTAJE_ISO/$f $MONTAJE_DEST/distros/sysrcd/
    done    
    echo "Tem Paciencia. Terminando de copiar los ficheros"
    sudo sync
    crearMenuLst
    crearSplash
    [ ! -e ./grub4dos-0.4.4 ] && descargaGrub4Dos && unzip /tmp/grub4dos-0.4.4.zip
    sudo cp ./grub4dos-0.4.4/[gG][rR][lL][dD][rR] $MONTAJE_DEST
    sudo umount $MONTAJE_DEST
    sudo umount $MONTAJE_ISO
    sudo ./grub4dos-0.4.4/bootlace.com --time-out=0 $DEST
    sudo sync
    fatlabel $DEST $LABEL
    sudo kvm -hda $DEST -m 512
}


uso() {
   echo uso: $0 '<dispositivo-donde-instalar> <path-sysrcd.iso> [<etiqueta-de-volumen-para-dispositivo-donde-instalar>]'
   echo Ej.: $0 /dev/sdc /tmp/sysrcd.iso RESCATE
   exit 1
}
descargaSysrcd() {
    TMP=$(tempfile)
    wget -O $TMP http://www.system-rescue-cd.org/Download/ 2>/dev/null
    URL=$(cat $TMP | grep -A 1 "Download link" | tail -1 | awk -F 'href="' '{print $2;}' | awk -F '</a>' '{print $1;}' | awk -F '">' '{print $1;}')
    MD5SUM=$(cat $TMP | grep -A 1 "md5sum" | egrep -o '[0-9a-zA-Z]{32}')
    ISO="/tmp/$(echo $URL | egrep -o 'systemrescuecd-.*iso')"
    [ "$(which axel)" = "" ] && sudo apt -y install axel &> /dev/null
    [ "$(which axel)" = "" ] && DOWNLOADER="wget --no-check-certificate -c --timeout=10 -q --show-progress -O $ISO " || DOWNLOADER="axel -k -n 15 --output=$ISO "
    #rm -rf /tmp/* &> /dev/null
    while [ "$MD5SUM" != "$MD5ISO" ]; do
        $DOWNLOADER $URL 1>&2
        MD5ISO=$(md5sum $ISO | awk '{print $1;}')
    done
    echo $ISO
}

descargaGrub4Dos() {
    wget -c https://raw.githubusercontent.com/javier-iesn/prj/master/grub4dos-0.4.4.zip -O /tmp/grub4dos-0.4.4.zip
} 

